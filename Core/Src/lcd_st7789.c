   uint32_t    LCD_TxDataCount;     // 魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ
   HAL_StatusTypeDef errorcode = HAL_OK;

	/* Check Direction parameter */
